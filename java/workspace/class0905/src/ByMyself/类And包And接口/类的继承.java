/*
继承（inheritance）是面向对象对象程序设计中最为重要的特征之一
子类（subclass）,父类或超类（superclass）
    父类包括所有直接或间接被继承的类
JAVA支持单继承：一个类只能有一个直接的父类

继承的好处：
子类继承父类的状态和行为
    可以修改父类的状态或重载父类的行为
    可以添加新的状态和行为
好处
    可以提高程序的抽象成都
    实现代码的重用，提高开发效率和可维护性

1、字段的继承
    *子类可以父类的所有字段
2、字段的隐藏
    *子类重新定义一个与父类哪里继承来的域变量完全相同的变量，称为域的
    隐藏。域的隐藏在实际编程中使用的较小
3、字段的添加
    *在定义子类是，添加上新的域变量，就可以使子类多一些属性

1、方法的继承
2、方法的覆盖（Override）
 */