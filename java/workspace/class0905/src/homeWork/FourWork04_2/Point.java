package homeWork.FourWork04_2;

public class Point {
    private double x;
    private double y;
    public Point(){};
    public Point(double a1,double a2){
        x=a1;
        y=a2;
    }
    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
