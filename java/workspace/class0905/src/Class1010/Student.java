package Class1010;

public class Student {
    private String no;
    private String name;
    private int score;

    public int getScore() {
        return score;
    }

    public String getNo() {
        return no;
    }

    public String getName() {
        return name;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public void setName(String name) {
        this.name = name;
    }
}
